
a=(int)(input("Enter the first number:"))
b=(int)(input("Enter the Second Number:"))

c=input("Enter Operation To be Performed :")

if c=='+':
    print("Addition of Given Numbers is:\n",a+b)
elif c=='-':
    print("Subtraction of Given Numbers is:\n",a-b)
elif c=='*':
    print("Multiplication of Given Numbers is :\n",a*b)
elif c=='/':
    print("Division of Given Numbers is :\n",a/b)

    